package UDP;

import java.io.Serializable;

/**
 *
 * @author TuanVH
 */
public class Product937 implements Serializable {

    private static final long serialVersionUID = 937;
    public String id;
    public String code;
    public String name;
    public int quantity;

    public Product937(String id, String code, String name, int quantity) {
        this.id = id;
        this.code = code;
        this.name = name;
        this.quantity = quantity;
    }

    public Product937(String code) {
        this.code = code;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    @Override
    public boolean equals(Object obj) {
        Product937 other = (Product937) obj;
        return this.id.equals(other.id) && this.name.equals(other.name) && this.quantity == other.quantity;

    }

}
